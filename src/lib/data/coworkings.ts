    region,
    address: '',
    zipCode: '',
    latitude: null,
    longitude: null,
    capacity: 20,
    areaM2: 500,
    phone: '',
    email: '',
    website: '',
    openingHours: defaultOpeningHours,
    priceDayPass: 299,
    priceMonthly: 3990,
    priceHourly: 99,
    platformTier: null,
    platformActive: false,
    isActive: true,
    isVerified: false,
    isFeatured: false,
    amenities: ['wifi', 'meeting_rooms', 'kitchen', 'printer', 'reception'],
    photos: [],
    locations: [],
